package org.springframework.social.zotero.api;

public class Links {

    private String href;
    private String type;
    
    public String getHref() {
        return href;
    }
    public void setHref(String href) {
        this.href = href;
    }
    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }
    
}
